package io.github.jaeyeonling.saju.astronomy

import kotlin.math.pow

/**
 * ΔT = TT − UT (초). Fred Espenak & Jean Meeus(2006) 구간별 다항식.
 *
 * 출처: NASA Eclipse(Espenak-Meeus). commenthol/astronomia `deltat.js` 가 2005~2050 구간에서
 * 동일 계수(`horner(u, 62.92, 32.217, 55.89)`, u=(year−2000)/100)를 쓰는 것을 확인했다.
 * 사주 v1 범위(1391~2100)를 매끄러운 다항식으로 커버하며, 분 단위 정밀도엔 충분하다
 * (현대 구간은 실측 대비 1초 이내, ΔT 수 초 오차는 절기 시각 분 단위에 무영향).
 */
internal object DeltaT {

    /** [decimalYear] (예: 2026.5)에서 ΔT (초). */
    fun seconds(decimalYear: Double): Double {
        val y = decimalYear
        return when {
            y < -500.0 -> farPast(y)
            y < 500.0 -> horner(y / 100.0, 10583.6, -1014.41, 33.78311, -5.952053, -0.1798452, 0.022174192, 0.0090316521)
            y < 1600.0 -> horner((y - 1000.0) / 100.0, 1574.2, -556.01, 71.23472, 0.319781, -0.8503463, -0.005050998, 0.0083572073)
            y < 1700.0 -> (y - 1600.0).let { t -> horner(t, 120.0, -0.9808, -0.01532) + t * t * t / 7129.0 }
            y < 1800.0 -> (y - 1700.0).let { t -> horner(t, 8.83, 0.1603, -0.0059285, 0.00013336) - t.pow(4) / 1174000.0 }
            y < 1860.0 -> horner(
                y - 1800.0,
                13.72, -0.332447, 0.0068612, 0.0041116, -0.00037436, 0.0000121272, -0.0000001699, 0.000000000875,
            )
            y < 1900.0 -> (y - 1860.0).let { t -> horner(t, 7.62, 0.5737, -0.251754, 0.01680668, -0.0004473624) + t.pow(5) / 233174.0 }
            y < 1920.0 -> horner(y - 1900.0, -2.79, 1.494119, -0.0598939, 0.0061966, -0.000197)
            y < 1941.0 -> horner(y - 1920.0, 21.20, 0.84493, -0.076100, 0.0020936)
            y < 1961.0 -> (y - 1950.0).let { t -> 29.07 + 0.407 * t - t * t / 233.0 + t * t * t / 2547.0 }
            y < 1986.0 -> (y - 1975.0).let { t -> 45.45 + 1.067 * t - t * t / 260.0 - t * t * t / 718.0 }
            y < 2005.0 -> horner(y - 2000.0, 63.86, 0.3345, -0.060374, 0.0017275, 0.000651814, 0.00002373599)
            y < 2050.0 -> horner(y - 2000.0, 62.92, 0.32217, 0.005589)
            y < 2150.0 -> -20.0 + 32.0 * ((y - 1820.0) / 100.0).pow(2) - 0.5628 * (2150.0 - y)
            else -> farPast(y)
        }
    }

    /** 먼 과거/미래 외삽: ΔT = −20 + 32·u², u=(year−1820)/100. */
    private fun farPast(year: Double): Double {
        val u = (year - 1820.0) / 100.0
        return -20.0 + 32.0 * u * u
    }
}
